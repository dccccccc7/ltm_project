package com.example.mes.WorkshopStorage.para;

public class LinePara {
    private String newLineId;
    private String newLineName;
    private String newLineRouting;
    private String Pre_id;

    public String getPre_id() {
        return Pre_id;
    }

    public void setPre_id(String pre_id) {
        Pre_id = pre_id;
    }

    public String getNewLineId() {
        return newLineId;
    }

    public void setNewLineId(String newLineId) {
        this.newLineId = newLineId;
    }

    public String getNewLineName() {
        return newLineName;
    }

    public void setNewLineName(String newLineName) {
        this.newLineName = newLineName;
    }

    public String getNewLineRouting() {
        return newLineRouting;
    }

    public void setNewLineRouting(String newLineRouting) {
        this.newLineRouting = newLineRouting;
    }
}
